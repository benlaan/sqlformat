namespace Laan.Sql.Formatter
{
    /// <summary>
    /// Specifies the spacing style for brackets/parentheses
    /// </summary>
    public enum BracketSpacing
    {
        /// <summary>
        /// No spaces within brackets: (value)
        /// </summary>
        NoSpaces,

        /// <summary>
        /// Spaces within brackets: ( value )
        /// </summary>
        WithSpaces
    }
}
